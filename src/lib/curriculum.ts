export function getCurriculumStats() {
  const modules = curriculum.reduce(
    (sum, grade) => sum + grade.algebra.length + grade.geometry.length,
    0,
  );
  const lessons = curriculum.reduce(
    (sum, grade) =>
      sum +
      [...grade.algebra, ...grade.geometry].reduce(
        (moduleSum, module) => moduleSum + module.lessons,
        0,
      ),
    0,
  );
  const homework = curriculum.reduce(
    (sum, grade) =>
      sum +
      [...grade.algebra, ...grade.geometry].reduce(
        (moduleSum, module) => moduleSum + module.homework,
        0,
      ),
    0,
  );

  return { modules, lessons, homework };
}
